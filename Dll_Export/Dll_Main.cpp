#include "Dll_Main.h"

double Sum(double dData1, double dData2) {
	return dData1 + dData2;
}

double Sub(double dData1, double dData2) {
	return dData1 - dData2;
}

double Mul(double dData1, double dData2) {
	return dData1 * dData2;
}

double Div(double dData1, double dData2) {
	return dData1 / dData2;
}